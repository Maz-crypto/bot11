"""Guess the image type from a file or byte stream.

This module recognizes the following image types:
    * RGB bitmap files ('rgb')
    * GIF files ('gif')
    * Portable Network Graphics ('png')
    * JPEG data ('jpeg')
    * TIFF files ('tiff')
    * SGI Image File Format ('sgi')
    * Sun Rasterfile ('sun')
    * X bitmap files ('xbm')
    * WebP files ('webp')
    * AVIF files ('avif')

The 'tests' argument to the what() and test functions is a
sequence of functions to be tried.  The functions are called
with two arguments: the byte stream and the file name.  If the
file name is not known, it is None.  The functions should return
a string describing the image type if recognized, and None if not.
"""

__all__ = ["what", "test"]

# Known image tests
tests = []

def test_jpeg(h, f):
    """JPEG data in JFIF or Exif format"""
    if h[6:10] in (b'JFIF', b'Exif'):
        return 'jpeg'

tests.append(test_jpeg)

def test_png(h, f):
    if h.startswith(b'\211PNG\r\n\032\n'):
        return 'png'

tests.append(test_png)

def test_gif(h, f):
    """GIF ('87 and '89 variants)"""
    if h[:6] in (b'GIF87a', b'GIF89a'):
        return 'gif'

tests.append(test_gif)

def test_tiff(h, f):
    """TIFF (can be in Motorola or Intel byte order)"""
    if h[:2] in (b'MM', b'II'):
        return 'tiff'

tests.append(test_tiff)

def test_rgb(h, f):
    """SGI image library"""
    if h.startswith(b'\001\332'):
        return 'rgb'

tests.append(test_rgb)

def test_pbm(h, f):
    """PBM (portable bitmap)"""
    if len(h) >= 3 and \
        h[0] == ord(b'P') and h[1] in b'14' and h[2] in b' \t\n\r':
        return 'pbm'

tests.append(test_pbm)

def test_pgm(h, f):
    """PGM (portable graymap)"""
    if len(h) >= 3 and \
        h[0] == ord(b'P') and h[1] in b'25' and h[2] in b' \t\n\r':
        return 'pgm'

tests.append(test_pgm)

def test_ppm(h, f):
    """PPM (portable pixmap)"""
    if len(h) >= 3 and \
        h[0] == ord(b'P') and h[1] in b'36' and h[2] in b' \t\n\r':
        return 'ppm'

tests.append(test_ppm)

def test_rast(h, f):
    """Sun raster file"""
    if h.startswith(b'\x59\xA6\x6A\x95'):
        return 'rast'

tests.append(test_rast)

def test_xbm(h, f):
    """X bitmap (X10 or X11)"""
    s = b'#define '
    if h.startswith(s):
        return 'xbm'

tests.append(test_xbm)

def test_bmp(h, f):
    if h.startswith(b'BM'):
        return 'bmp'

tests.append(test_bmp)

def test_webp(h, f):
    if h[8:12] == b'WEBP':
        return 'webp'

tests.append(test_webp)

def test_exr(h, f):
    if h.startswith(b'\x76\x2f\x31\x01'):
        return 'exr'

tests.append(test_exr)

def test_avif(h, f):
    if h[4:12] == b'ftypavif':
        return 'avif'

tests.append(test_avif)

# ---- Exported functions ----

def what(file, h=None):
    """Guess the image type given a file path or byte stream.

    Args:
        file: Path to file, or byte stream.
        h: Optional bytes header (first 32 bytes recommended).

    Returns:
        str or None: Image type (e.g., 'png', 'jpeg'), or None.
    """
    f = None
    try:
        if h is None:
            if isinstance(file, (str, bytes)):
                f = open(file, 'rb')
                h = f.read(32)
            else:
                location = file.tell()
                h = file.read(32)
                file.seek(location)
        for tf in tests:
            res = tf(h, f)
            if res:
                return res
    finally:
        if f: f.close()
    return None

def test(h, f=None):
    """Test the image header `h` with all registered tests."""
    for tf in tests:
        res = tf(h, f)
        if res:
            return res
    return None
